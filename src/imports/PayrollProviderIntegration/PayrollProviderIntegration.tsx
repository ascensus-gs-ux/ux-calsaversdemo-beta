function Form() {
  const [searchQuery, setSearchQuery] = useState("");
  const [errorMessage, setErrorMessage] = useState("");
  const [successMessage, setSuccessMessage] = useState("");
  const [showDropdown, setShowDropdown] = useState(false);

  const filteredProviders = searchQuery.trim()
    ? PAYROLL_PROVIDERS.filter((provider) =>
        provider.toLowerCase().includes(searchQuery.toLowerCase())
      ).slice(0, 10)
    : [];

  const handleSearchChange = (value: string) => {
    setSearchQuery(value);
    setShowDropdown(value.trim().length > 0);
    setSuccessMessage("");
    if (!value.trim()) {
      setErrorMessage("");
      return;
    }
    const matches = PAYROLL_PROVIDERS.filter((provider) =>
      provider.toLowerCase().includes(value.toLowerCase())
    );
    if (matches.length === 0 && value.length > 2) {
      setErrorMessage("Your payroll provider is not yet participating");
    } else {
      setErrorMessage("");
    }
  };

  const handleSelectProvider = (provider: string) => {
    setSearchQuery(provider);
    setShowDropdown(false);
    setErrorMessage("");
    setSuccessMessage("Good news! Your selected payroll provider is offered through Payroll Integrations Inc.");
  };

  return (
    <div className="d-flex flex-column gap-3 align-items-start position-relative flex-shrink-0 w-100" style={{zIndex: 2}} data-name="form">
      <div className="d-flex flex-column gap-2 align-items-start position-relative flex-shrink-0 w-100" data-name="Dropdown/PI">
        <Label />
        <DropdownGroup
          value={searchQuery}
          onChange={handleSearchChange}
          hasError={!!errorMessage}
          filteredProviders={filteredProviders}
          onSelect={handleSelectProvider}
          showDropdown={showDropdown}
        />
        {successMessage ? <SuccessText message={successMessage} /> : <AssistiveText message={errorMessage} />}
      </div>
      <p className="fst-italic w-100 flex-shrink-0" style={{color: '#6B6B6B', fontSize: '14px'}}>
        <sup>*</sup>{`If you use Paychex or TJH Hull for your payroll services, see this instruction in the "Learn more about Paychex and TJH Payroll integration" section.`}
      </p>
    </div>
  );
}

